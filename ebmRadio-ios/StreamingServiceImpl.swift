import Foundation
import AVFoundation

class StreamingServiceImpl: StreamingService {
    
    private var player = AVPlayer(URL: NSURL(string: "http://ebm-radio.org:7000/listen.pls")!)
    var isPlaying = false
    
    func play() {
        player.play()
        isPlaying = true
    }
    
    func pause() {
        player.pause()
        isPlaying = false
    }
    
    func toggle() {
        if isPlaying == true {
            pause()
        } else {
            play()
        }
    }
    
    func currentlyPlaying() -> Bool {
        return isPlaying
    }
    
}